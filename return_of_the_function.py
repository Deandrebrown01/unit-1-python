"""
Task 3: Convert Temperature from Celsius to Fahrenheit:
Write a function that takes a temperature in Celsius and returns 
the equivalent temperature in Fahrenheit using the correct formula
"""
def my_temp(d):
    return(d * 9/5) + 32
z=my_temp(80)
print(z)

